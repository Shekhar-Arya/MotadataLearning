package com.company;

public class Main {

    public static void main(String[] args) {

        // Following are the Primitive which are built in Java which helps to store different type of values
        //Range from -128 to 127
        byte a = 12;

        //Range -32768 to 32767
        short b = 12345;

        // Range from -2^31 to 2^31-1
        int c = 1234567;

        // Range from -2^64 to 2^64-1
        long d = 1234567890;

        // To store decimal values
        float e = 1.2f;

        double f = 1234.3344;

        // Storing boolean values
        boolean g = true;

        // Used to store a single character
        char h = 'A';

        // To store whole string we will use String double qutation will automatically create new String.
        String i = "Shekhar";

        // Default value of Primitive datatypes will be as follow:
        //byte - 0
        //short - 0
        //int  - 0
        // long - 0
        // float - 0.0f
        //double - 0.0d
        // String - null
        // boolean - false

        // This default value only be provided to those variabels which are not local and not parameter.
    }
}
